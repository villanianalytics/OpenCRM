CREATE TABLE IF NOT EXISTS roles (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, name VARCHAR(80) NOT NULL UNIQUE,
 permissions_json JSON NOT NULL, created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS users (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, role_id BIGINT UNSIGNED NULL,
 username VARCHAR(80) NOT NULL UNIQUE, display_name VARCHAR(120) NULL, email VARCHAR(190) NULL UNIQUE, password_hash VARCHAR(255) NOT NULL,
 is_admin BOOLEAN NOT NULL DEFAULT FALSE, force_password_change BOOLEAN NOT NULL DEFAULT TRUE,
 active BOOLEAN NOT NULL DEFAULT TRUE, partner_sales_access BOOLEAN NOT NULL DEFAULT FALSE, last_login_at DATETIME NULL, created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
 FOREIGN KEY (role_id) REFERENCES roles(id) ON DELETE SET NULL
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS tags (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, name VARCHAR(100) NOT NULL UNIQUE, color CHAR(7) NOT NULL DEFAULT '#546e7a', created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS tag_permissions (
 role_id BIGINT UNSIGNED NOT NULL, tag_id BIGINT UNSIGNED NOT NULL,
 access_level ENUM('hidden','read','write') NOT NULL DEFAULT 'write', PRIMARY KEY(role_id,tag_id),
 FOREIGN KEY(role_id) REFERENCES roles(id) ON DELETE CASCADE, FOREIGN KEY(tag_id) REFERENCES tags(id) ON DELETE CASCADE
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS contacts (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, first_name VARCHAR(100) NOT NULL, last_name VARCHAR(100) NOT NULL,
 company VARCHAR(190) NULL, job_title VARCHAR(150) NULL, email VARCHAR(190) NULL, phone VARCHAR(60) NULL,
 address TEXT NULL, website VARCHAR(255) NULL, active BOOLEAN NOT NULL DEFAULT TRUE, created_by BIGINT UNSIGNED NULL, created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
 updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP, INDEX(last_name), INDEX(company), INDEX(email),
 FOREIGN KEY(created_by) REFERENCES users(id) ON DELETE SET NULL
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS companies (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, name VARCHAR(190) NOT NULL UNIQUE,
 website VARCHAR(255) NULL, phone VARCHAR(60) NULL, address TEXT NULL, notes TEXT NULL,
 owner_id BIGINT UNSIGNED NULL, created_by BIGINT UNSIGNED NULL, created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
 updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
 FOREIGN KEY(owner_id) REFERENCES users(id) ON DELETE SET NULL
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS contact_tags (
 contact_id BIGINT UNSIGNED NOT NULL, tag_id BIGINT UNSIGNED NOT NULL, PRIMARY KEY(contact_id,tag_id),
 FOREIGN KEY(contact_id) REFERENCES contacts(id) ON DELETE CASCADE, FOREIGN KEY(tag_id) REFERENCES tags(id) ON DELETE CASCADE
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS opportunities (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, contact_id BIGINT UNSIGNED NOT NULL, owner_id BIGINT UNSIGNED NULL, created_by BIGINT UNSIGNED NULL, title VARCHAR(190) NOT NULL,
 score ENUM('high','medium','low','keep_in_touch','not_buyer') NOT NULL DEFAULT 'medium', value DECIMAL(12,2) NULL,
 status ENUM('open','won','lost') NOT NULL DEFAULT 'open', source_type ENUM('none','event','other') NOT NULL DEFAULT 'none',
 source_event_id BIGINT UNSIGNED NULL, source_note TEXT NULL, expected_close_date DATE NULL, probability TINYINT UNSIGNED NULL, active BOOLEAN NOT NULL DEFAULT TRUE, closed_at DATETIME NULL, created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
 FOREIGN KEY(contact_id) REFERENCES contacts(id) ON DELETE CASCADE
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS opportunity_contacts (
 opportunity_id BIGINT UNSIGNED NOT NULL, contact_id BIGINT UNSIGNED NOT NULL,
 PRIMARY KEY(opportunity_id,contact_id), FOREIGN KEY(opportunity_id) REFERENCES opportunities(id) ON DELETE CASCADE,
 FOREIGN KEY(contact_id) REFERENCES contacts(id) ON DELETE CASCADE
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS partner_performance_reviews (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, partner_user_id BIGINT UNSIGNED NOT NULL, reviewer_user_id BIGINT UNSIGNED NULL,
 decision VARCHAR(30) NOT NULL, rating TINYINT UNSIGNED NULL, notes TEXT NULL, review_date DATE NOT NULL,
 created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP, FOREIGN KEY(partner_user_id) REFERENCES users(id) ON DELETE CASCADE,
 FOREIGN KEY(reviewer_user_id) REFERENCES users(id) ON DELETE SET NULL, INDEX(partner_user_id,review_date)
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS notes (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, contact_id BIGINT UNSIGNED NOT NULL, user_id BIGINT UNSIGNED NULL,
 body TEXT NOT NULL, created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
 FOREIGN KEY(contact_id) REFERENCES contacts(id) ON DELETE CASCADE, FOREIGN KEY(user_id) REFERENCES users(id) ON DELETE SET NULL
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS contact_saved_views (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, user_id BIGINT UNSIGNED NOT NULL, name VARCHAR(100) NOT NULL,
 filters_json JSON NOT NULL, is_default BOOLEAN NOT NULL DEFAULT FALSE, position INT NOT NULL DEFAULT 100,
 created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP, updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
 UNIQUE KEY(user_id,name), FOREIGN KEY(user_id) REFERENCES users(id) ON DELETE CASCADE
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS contact_relationships (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, contact_id BIGINT UNSIGNED NOT NULL, related_contact_id BIGINT UNSIGNED NOT NULL,
 relationship_type VARCHAR(80) NOT NULL DEFAULT 'Related contact', notes VARCHAR(255) NULL, created_by BIGINT UNSIGNED NULL,
 created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP, UNIQUE KEY(contact_id,related_contact_id),
 FOREIGN KEY(contact_id) REFERENCES contacts(id) ON DELETE CASCADE, FOREIGN KEY(related_contact_id) REFERENCES contacts(id) ON DELETE CASCADE,
 FOREIGN KEY(created_by) REFERENCES users(id) ON DELETE SET NULL
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS contact_field_changes (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, contact_id BIGINT UNSIGNED NOT NULL, user_id BIGINT UNSIGNED NULL,
 field_name VARCHAR(100) NOT NULL, old_value TEXT NULL, new_value TEXT NULL, created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
 FOREIGN KEY(contact_id) REFERENCES contacts(id) ON DELETE CASCADE, FOREIGN KEY(user_id) REFERENCES users(id) ON DELETE SET NULL,
 INDEX(contact_id,created_at)
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS events (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, title VARCHAR(190) NOT NULL, event_type ENUM('webinar','conference','presentation','other') NOT NULL,
 starts_at DATETIME NOT NULL, presentation_title VARCHAR(190) NULL, abstract TEXT NULL,
 booking_url VARCHAR(500) NULL, conference_url VARCHAR(500) NULL, venue_address TEXT NULL,
 slides_path VARCHAR(255) NULL, slides_original_name VARCHAR(255) NULL, presenter_contact_id BIGINT UNSIGNED NULL, presenter_name VARCHAR(190) NULL,
 created_by BIGINT UNSIGNED NULL, owner_id BIGINT UNSIGNED NULL, created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
 FOREIGN KEY(created_by) REFERENCES users(id) ON DELETE SET NULL, FOREIGN KEY(presenter_contact_id) REFERENCES contacts(id) ON DELETE SET NULL
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS event_attendees (
 event_id BIGINT UNSIGNED NOT NULL, contact_id BIGINT UNSIGNED NOT NULL, attended BOOLEAN NOT NULL DEFAULT TRUE,
 PRIMARY KEY(event_id,contact_id), FOREIGN KEY(event_id) REFERENCES events(id) ON DELETE CASCADE,
 FOREIGN KEY(contact_id) REFERENCES contacts(id) ON DELETE CASCADE
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS event_presenters (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, event_id BIGINT UNSIGNED NOT NULL,
 presenter_type ENUM('user','contact') NOT NULL, presenter_id BIGINT UNSIGNED NOT NULL,
 created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP, UNIQUE(event_id,presenter_type,presenter_id),
 FOREIGN KEY(event_id) REFERENCES events(id) ON DELETE CASCADE
) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS saved_reports (
 id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY, user_id BIGINT UNSIGNED NOT NULL, name VARCHAR(150) NOT NULL,
 chart_type ENUM('bar','pie','doughnut') NOT NULL DEFAULT 'bar', group_by VARCHAR(40) NOT NULL,
 filters_json JSON NOT NULL, shared BOOLEAN NOT NULL DEFAULT FALSE, created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
 FOREIGN KEY(user_id) REFERENCES users(id) ON DELETE CASCADE
